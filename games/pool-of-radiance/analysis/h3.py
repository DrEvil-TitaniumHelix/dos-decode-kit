import struct
from capstone import Cs, CS_ARCH_X86, CS_MODE_16
GAME=r".\games\pool-of-radiance\extracted\poolrad"
ex=open(GAME+r"\START.EXE","rb").read()
md=Cs(CS_ARCH_X86, CS_MODE_16)
def resident(seg,off): return 0x200+seg*16+off
# Show raw bytes after int 0x3f for each stub: FBOV stub = CD 3F <ovr_id byte> <offset word> <?>
for name,seg,off in [("0x2b:0x25",0x2b,0x25),("0x2b:0x2a",0x2b,0x2a),("0x2b:0x4d",0x2b,0x4d),
                     ("0x2b:0x5c",0x2b,0x5c),("0x2b:0x8e",0x2b,0x8e),
                     ("0xba:0x1073",0xba,0x1073),("0x709:0x768",0x709,0x768),
                     ("0x709:0x9e3",0x709,0x9e3),("0x3c5:0x3e",0x3c5,0x3e),
                     ("0x3d5:0x43",0x3d5,0x43),("0x3d5:0x3e",0x3d5,0x3e),("0x3e9:0x25",0x3e9,0x25)]:
    fo=resident(seg,off)
    raw=ex[fo:fo+12]
    print(f"{name:14s} file 0x{fo:05X}: "+" ".join(f"{b:02X}" for b in raw))
